            //         }
            //     });
        },
        methods: {
            logout: function () {
                axios.post('/logout')
                .then(response => {
                    location.href = '/';
                });
            },
            markAsRead: function () {
                axios.get('/dashboard/markAsRead')
                .then(response => {
                    this.unReadNotifications = [];
                });
            }
        }
    }
</script>
